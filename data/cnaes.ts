export function cnaeToSetor(cnae: string): string {
  const prefix = cnae.substring(0, 4)
  for (const setor of SETORES) {
    if (setor.cnaes.some(c => prefix.startsWith(c.substring(0, 2)))) {
      return setor.label
    }
  }
  return 'Outros'
}
